function renderTurnRulesEngineScript(): string {
  return [
    "extends RefCounted",
    "class_name TurnRulesEngine",
    "",
    "const TOKEN_COUNT = 4",
    "const TRACK_LENGTH = 52",
    "const HOME_LENGTH = 6",
    "const FINISH_STEPS = TRACK_LENGTH + HOME_LENGTH - 1",
    "const PLAYER_COLORS = [\"red\", \"blue\", \"green\", \"yellow\"]",
    "const START_OFFSETS = [0, 13, 26, 39]",
    "const SAFE_SQUARES = [0, 8, 13, 21, 26, 34, 39, 47]",
    "",
    "func create_initial_state(player_count := 4, win_token_target := 2):",
    "    player_count = clamp(player_count, 2, 4)",
    "    win_token_target = clamp(win_token_target, 1, TOKEN_COUNT)",
    "    var players = []",
    "    for player_index in range(player_count):",
    "        var tokens = []",
    "        for token_index in range(TOKEN_COUNT):",
    "            tokens.append({\"steps\": -1, \"home\": false})",
    "        players.append({\"id\": player_index, \"color\": PLAYER_COLORS[player_index], \"tokens\": tokens})",
    "    return {",
    "        \"player_count\": player_count,",
    "        \"turn\": 0,",
    "        \"dice\": 0,",
    "        \"phase\": \"await_roll\",",
    "        \"winner\": -1,",
    "        \"win_token_target\": win_token_target,",
    "        \"players\": players,",
    "        \"move_history\": []",
    "    }",
    "",
    "func roll(state, dice_value):",
    "    if state[\"phase\"] == \"end_match\":",
    "        return {\"ok\": false, \"reason\": \"match_complete\"}",
    "    dice_value = int(dice_value)",
    "    if dice_value < 1 or dice_value > 6:",
    "        return {\"ok\": false, \"reason\": \"invalid_dice\"}",
    "    state[\"dice\"] = dice_value",
    "    state[\"phase\"] = \"select_token\"",
    "    return {\"ok\": true, \"legal_moves\": get_legal_moves(state)}",
    "",
    "func get_legal_moves(state):",
    "    var legal = []",
    "    if state.get(\"phase\", \"await_roll\") == \"await_roll\":",
    "        return legal",
    "    var dice = int(state.get(\"dice\", 0))",
    "    var player = state[\"players\"][state[\"turn\"]]",
    "    for token_index in range(player[\"tokens\"].size()):",
    "        var token = player[\"tokens\"][token_index]",
    "        if token[\"home\"]:",
    "            continue",
    "        if token[\"steps\"] == -1:",
    "            if dice == 6:",
    "                legal.append(token_index)",
    "        elif token[\"steps\"] + dice <= FINISH_STEPS:",
    "            legal.append(token_index)",
    "    return legal",
    "",
    "func apply_move(state, token_index):",
    "    token_index = int(token_index)",
    "    var legal = get_legal_moves(state)",
    "    if not legal.has(token_index):",
    "        return {\"ok\": false, \"reason\": \"illegal_move\", \"legal_moves\": legal}",
    "    var player_index = state[\"turn\"]",
    "    var player = state[\"players\"][player_index]",
    "    var token = player[\"tokens\"][token_index]",
    "    var events = []",
    "    if token[\"steps\"] == -1:",
    "        token[\"steps\"] = 0",
    "        events.append(\"released_from_base\")",
    "    else:",
    "        token[\"steps\"] += int(state[\"dice\"])",
    "    if token[\"steps\"] == FINISH_STEPS:",
    "        token[\"home\"] = true",
    "        events.append(\"home\")",
    "    elif token[\"steps\"] < TRACK_LENGTH:",
    "        events += _resolve_capture(state, player_index, token[\"steps\"])",
    "    state[\"move_history\"].append({",
    "        \"player\": player_index,",
    "        \"token\": token_index,",
    "        \"dice\": state[\"dice\"],",
    "        \"events\": events.duplicate()",
    "    })",
    "    if _player_complete(state, player):",
    "        state[\"phase\"] = \"end_match\"",
    "        state[\"winner\"] = player_index",
    "    elif int(state[\"dice\"]) == 6:",
    "        state[\"phase\"] = \"await_roll\"",
    "    else:",
    "        state[\"turn\"] = (state[\"turn\"] + 1) % state[\"player_count\"]",
    "        state[\"phase\"] = \"await_roll\"",
    "    state[\"dice\"] = 0",
    "    return {\"ok\": true, \"events\": events}",
    "",
    "func pass_turn(state):",
    "    if get_legal_moves(state).size() > 0:",
    "        return {\"ok\": false, \"reason\": \"legal_move_exists\"}",
    "    state[\"move_history\"].append({\"player\": state[\"turn\"], \"token\": -1, \"dice\": state[\"dice\"], \"events\": [\"no_legal_move\"]})",
    "    state[\"turn\"] = (state[\"turn\"] + 1) % state[\"player_count\"]",
    "    state[\"dice\"] = 0",
    "    state[\"phase\"] = \"await_roll\"",
    "    return {\"ok\": true}",
    "",
    "func board_position(player_index, steps):",
    "    if int(steps) < 0 or int(steps) >= TRACK_LENGTH:",
    "        return -1",
    "    return (START_OFFSETS[player_index] + int(steps)) % TRACK_LENGTH",
    "",
    "func is_safe_square(position):",
    "    return SAFE_SQUARES.has(int(position))",
    "",
    "func _resolve_capture(state, player_index, steps):",
    "    var events = []",
    "    var position = board_position(player_index, steps)",
    "    if is_safe_square(position):",
    "        events.append(\"safe_square\")",
    "        return events",
    "    for other_index in range(state[\"players\"].size()):",
    "        if other_index == player_index:",
    "            continue",
    "        for token in state[\"players\"][other_index][\"tokens\"]:",
    "            if token[\"home\"] or token[\"steps\"] < 0 or token[\"steps\"] >= TRACK_LENGTH:",
    "                continue",
    "            if board_position(other_index, token[\"steps\"]) == position:",
    "                token[\"steps\"] = -1",
    "                token[\"home\"] = false",
    "                events.append(\"capture\")",
    "    return events",
    "",
    "func _player_complete(state, player):",
    "    return token_counts(player)[\"home\"] >= int(state.get(\"win_token_target\", TOKEN_COUNT))",
    "",
    "func token_counts(player):",
    "    var counts = {\"base\": 0, \"active\": 0, \"home\": 0}",
    "    for token in player[\"tokens\"]:",
    "        if token[\"home\"]:",
    "            counts[\"home\"] += 1",
    "        elif token[\"steps\"] == -1:",
    "            counts[\"base\"] += 1",
    "        else:",
    "            counts[\"active\"] += 1",
    "    return counts"
  ].join("\n");
}

